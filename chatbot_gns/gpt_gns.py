def obtener_historial(sender_id, limite=10):
    """Recupera el historial de mensajes recientes del usuario de forma cronol√≥gica."""
    conexion = conectar_db()
    if not conexion:
        return []
    try:
        with conexion.cursor(cursor_factory=RealDictCursor) as cursor:
            query = """
                SELECT mensaje, es_respuesta, timestamp
                FROM mensajes
                WHERE sender_id = %s
                ORDER BY timestamp DESC
                LIMIT %s
            """
            cursor.execute(query, (sender_id, limite))
            historial = cursor.fetchall()
            return historial[::-1]
    except Exception as e:
        logging.error(f"‚ùå Error al obtener historial: {e}")
        return []
    finally:
        conexion.close()

